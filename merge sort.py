def merge_sort(arr):
    """
    Сортировка слиянием (Merge Sort)

    Алгоритм использует стратегию "разделяй и властвуй":
    1. Делит массив на две равные половины
    2. Рекурсивно сортирует каждую половину
    3. Сливает отсортированные половины в один отсортированный массив

    Аргументы:
        arr: список элементов для сортировки

    Возвращает:
        отсортированный список
    """

    # Базовый случай рекурсии: если в массиве 0 или 1 элемент,
    # то он уже отсортирован и дальнейшее деление не требуется
    if len(arr) <= 1:
        return arr

    # Вычисляем индекс середины массива для разделения на две части
    # Оператор // выполняет целочисленное деление
    mid = len(arr) // 2

    # Рекурсивно сортируем левую половину массива
    # arr[:mid] - срез от начала до mid (не включая mid)
    left = merge_sort(arr[:mid])

    # Рекурсивно сортируем правую половину массива
    # arr[mid:] - срез от mid до конца массива
    right = merge_sort(arr[mid:])

    # Возвращаем результат слияния двух отсортированных половин
    return merge(left, right)


def merge(left, right):
    """
    Слияние двух отсортированных массивов в один отсортированный

    Принцип работы:
    1. Создаём пустой результирующий массив
    2. Сравниваем первые элементы левого и правого массивов
    3. Меньший элемент перемещаем в результат
    4. Повторяем, пока один из массивов не опустеет
    5. Добавляем все оставшиеся элементы из непустого массива

    Аргументы:
        left: отсортированный левый массив
        right: отсортированный правый массив

    Возвращает:
        объединённый отсортированный массив
    """

    result = []  # Результирующий массив, куда будут собираться элементы
    i = 0  # Индекс текущего элемента в левом массиве
    j = 0  # Индекс текущего элемента в правом массиве

    # Пока не дошли до конца хотя бы одного из массивов
    while i < len(left) and j < len(right):
        # Сравниваем текущие элементы левого и правого массивов
        if left[i] <= right[j]:
            # Если элемент из левого массива меньше или равен,
            # добавляем его в результат
            result.append(left[i])
            i += 1  # Переходим к следующему элементу в левом массиве
        else:
            # Иначе элемент из правого массива меньше,
            # добавляем его в результат
            result.append(right[j])
            j += 1  # Переходим к следующему элементу в правом массиве

    # После завершения основного цикла в одном из массивов
    # могли остаться элементы

    # Добавляем все оставшиеся элементы из левого массива (если они есть)
    # left[i:] - срез от текущего индекса i до конца массива
    result.extend(left[i:])

    # Добавляем все оставшиеся элементы из правого массива (если они есть)
    result.extend(right[j:])

    return result


# Тестирование алгоритма на примере
# Создаём тестовый массив с неотсортированными числами
test = [64, 34, 25, 12, 22, 11, 90]

# Выводим исходный массив для наглядности
print("Исходный массив:", test)

# Вызываем функцию сортировки и сохраняем результат
sorted_test = merge_sort(test)

# Выводим отсортированный массив
print("Отсортированный:", sorted_test)
