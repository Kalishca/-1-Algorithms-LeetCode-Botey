import random

def partition(arr, low, high):
    """
    Разделяет отрезок массива arr[low..high] относительно опорного элемента.
    Используется схема Ломуто:
    - опорный элемент выбирается случайно и перемещается в конец отрезка;
    - все элементы, меньшие или равные опорному, собираются слева;
    - опорный ставится между меньшими/равными и большими.
    Возвращает индекс, на котором оказался опорный элемент после разделения.
    """
    # 1. Рандомизированный выбор опорного элемента
    rand_pivot = random.randint(low, high)
    # Перемещаем выбранный элемент в конец (чтобы использовать как опорный)
    arr[rand_pivot], arr[high] = arr[high], arr[rand_pivot]

    pivot_value = arr[high]   # значение опорного элемента
    i = low - 1               # индекс последнего элемента, который <= pivot

    # 2. Проходим по всем элементам, кроме последнего (он опорный)
    for j in range(low, high):
        if arr[j] <= pivot_value:
            i += 1
            arr[i], arr[j] = arr[j], arr[i]   # меньший элемент отправляем влево

    # 3. Ставим опорный элемент на своё место — между меньшими и большими
    arr[i + 1], arr[high] = arr[high], arr[i + 1]
    return i + 1   # индекс опорного после разделения


def quick_sort(arr, low, high):
    """
    Быстрая сортировка с рандомизированным выбором опорного элемента.
    Сортирует часть массива arr[low..high] на месте (in-place).
    """
    if low < high:
        # Получаем индекс опорного элемента после разделения
        pivot_idx = partition(arr, low, high)

        # Рекурсивно сортируем левую и правую части
        quick_sort(arr, low, pivot_idx - 1)
        quick_sort(arr, pivot_idx + 1, high)


# Пример использования
test_array = [3, 6, 1, 8, 2, 5]
print("Исходный массив:", test_array)
quick_sort(test_array, 0, len(test_array) - 1)
print("Отсортированный массив:", test_array)