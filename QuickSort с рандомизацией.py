import random

def partition(arr, low, high):
    # Выбираем случайный индекс в диапазоне [low, high]
    x = random.randint(low, high)
    # Перемещаем опорный элемент (с индексом x) в конец отрезка
    arr[x], arr[high] = arr[high], arr[x]
    # Теперь x — значение опорного элемента, стоящего в конце
    x = arr[high]

    i = low - 1  # индекс последнего элемента, который <= опорному
    # Проходим по всем элементам, кроме последнего (он опорный)
    for j in range(low, high):
        # Если текущий элемент меньше или равен опорному
        if arr[j] <= x:
            i += 1  # расширяем границу "меньших"
            arr[i], arr[j] = arr[j], arr[i]  # меняем местами
    # Ставим опорный элемент на своё место (между меньшими и большими)
    arr[i + 1], arr[high] = arr[high], arr[i + 1]
    return i + 1  # возвращаем индекс опорного после разделения

def quick_sort(arr, low, high):
    # Если в отрезке больше одного элемента
    if low < high:
        # Разделяем массив и получаем индекс опорного
        q = partition(arr, low, high)
        # Рекурсивно сортируем левую часть (до опорного)
        quick_sort(arr, low, q - 1)
        # Рекурсивно сортируем правую часть (после опорного)
        quick_sort(arr, q + 1, high)

# Пример использования
test_array = [3, 6, 1, 8, 2, 5]
print("Исходный массив:", test_array)
# Запускаем сортировку всего массива
quick_sort(test_array, 0, len(test_array) - 1)
print("Отсортированный массив:", test_array)